import UIKit

extension UIImage {
    
    static var sunIcon: UIImage? {
        UIImage(named: "sunIcon")
    }
    
    static var cloudsAndSunIcon: UIImage? {
        UIImage(named: "cloudsAndSun")
    }
    
    static var rainAndSunIcon : UIImage? {
        UIImage(named: "rainAndSunIcon")
    }

    static var cloudsIcon: UIImage? {
        UIImage(named: "cloudsIcon")
    }

    static var rainIcon: UIImage? {
        UIImage(named: "rainIcon")
    }

    static var thunderstormIcon: UIImage? {
        UIImage(named: "thunderstormIcon")
    }
    
    
    // for parts of the day
    
    static var termSun: UIImage? {
        UIImage(named: "termSun")
    }
    
    static var windIcon: UIImage? {
        UIImage(named: "windIcon")
    }
}
